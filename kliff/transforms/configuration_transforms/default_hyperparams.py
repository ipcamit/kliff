def get_bs_size(twojmax, diagonal):
    """
    Return the size of descriptor.
    """
    N = 0
    for j1 in range(0, twojmax + 1):
        if diagonal == 2:
            N += 1
        elif diagonal == 1:
            for j in range(0, min(twojmax, 2 * j1) + 1, 2):
                N += 1
        elif diagonal == 0:
            for j2 in range(0, j1 + 1):
                for j in range(j1 - j2, min(twojmax, j1 + j2) + 1, 2):
                    N += 1
        elif diagonal == 3:
            for j2 in range(0, j1 + 1):
                for j in range(j1 - j2, min(twojmax, j1 + j2) + 1, 2):
                    if j >= j1:
                        N += 1
    return N


